# Typecasting 
# The conversion of one datatype into other type. Python supports function like: int(), float(), str(), ord(), oct(), tuple(),list(), dict() etc for typecasting

# Types: Explicit, Implicit

a="3"
b= "4"
print(int(a)+int(b))#It's explicit typecasting

#Implicit typecasting
c=1.9
d=2#It's implicitly converted into float
print(c+d)



